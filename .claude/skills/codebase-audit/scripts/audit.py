#!/usr/bin/env python3
"""codebase-audit — emit ranked, evidence-rich health signals for an agent to judge.

NOT a gate and NOT a linter. It does the wide, quantitative gathering an agent
cannot cheaply do itself (it would have to read every file and run git log, burning
the context window) and prints a compact, ranked digest. The agent reads it and
decides what — if anything — to refactor. The script never says "too big"; it says
"here is where the mass, the change, and the coupling are, with evidence."

Fully repo-agnostic — drop it into any git repo and run it; no config, no setup.
Discovery is `git ls-files` (the version-controlled working tree), language is
detected by extension from a data-driven registry, and machine-generated files are
skipped via the universal `@generated` / "Generated by" / "Code generated" marker.

Signals (why each is here):
  * Decomposed size — raw LOC is the least informative number. A 790-line file that
    is 22 small types (a registry) is healthy; one where a single function is 400
    lines is not. So we emit LOC, largest-symbol span, biggest single function
    (`maxFn`), decl count, god ratio, nesting and density — measured at every depth
    so "big but flat" (many small methods) vs. "big & tangled" (one giant method)
    are actually distinguishable, which is the whole point.
  * Git-temporal — the half a static linter is blind to. churn, hotspot (size x
    churn — the classic "look here first"), and temporal coupling (files that always
    change together reveal a module boundary the directory tree doesn't show). This
    is established behavioral-code-analysis (CodeScene / Tornhill, Code as a Crime
    Scene), not invention.
  * Percentile ranking — "p99 LOC for this language" self-calibrates to the repo.

Symbol sizing is brace-/indentation-based and approximate (it says so in the
report). Function-level sizing needs a function *keyword*; languages without one
(C/C++, Java, C#) get type-level sizing only — their `maxFn` stays blank.

Stdlib only. Run from the repo root.
"""

from __future__ import annotations

import argparse
import json
import re
import subprocess
import sys
from collections import defaultdict
from dataclasses import dataclass, field
from pathlib import Path

# --------------------------------------------------------------------------- #
# Language registry — add a language by adding one row.                       #
# --------------------------------------------------------------------------- #
# style: "brace" (size symbols by { } depth) | "indent" (by indentation).
# decl:  regex with group(1)=kind, group(2)=name, or None (file-level metrics only).
#        Matched at every nesting level, so methods inside a type are measured too.
# funcs: which kinds count as "functions" for the biggest-function signal.
# A leading "(?:\w+\s+)*?" lazily skips modifier words (pub, final, nonisolated,
# export, async, ...) so we don't enumerate every language's modifier zoo.

_MODS = r"(?:@[\w.]+(?:\([^)]*\))?\s+)*(?:\w+\s+)*?"

# Function/method detector for keyword-less languages (TS/JS/Java/C#), where a
# function is `name(args) … {` or `const name = (…) => {` rather than `func name`.
# High-precision on purpose: it requires the body brace to open at end of line and
# excludes control keywords, so `if (x) {`, calls, plain `let x = 0`, and arrow
# callbacks don't masquerade as functions. It will miss bodies whose `{` is on the
# next line — a gap is safer than a false signal. group(1)=arrow-const name (or
# None), group(2)=method name (or None).
_METHOD = re.compile(
    r"^\s*(?:"
    # const/let/var NAME = (…) => {  |  = async (…) => {  |  = function  |  = x => {
    r"(?:export\s+)?(?:const|let|var)\s+([A-Za-z_$][\w$]*)\s*(?::\s*[^=]+?)?=\s*(?:async\s+)?"
    r"(?:function\b|[\w$]*\s*=>|\([^()]*\)\s*(?::[^={]+)?=>)[^{};]*\{\s*$"
    r"|"
    # [modifiers] [return-type] NAME(args) … {   (class / object method)
    r"(?:(?:public|private|protected|internal|static|final|abstract|virtual|override|"
    r"async|get|set|readonly|export|default|sealed|partial|new)\s+)*"
    r"(?:[\w<>\[\].,&*:]+\s+)?"
    r"(?!(?:if|for|while|switch|catch|return|else|do|with|new|typeof|await|throw|case|in|of|function)\b)"
    r"([A-Za-z_$][\w$]*)\s*\([^;{}]*\)\s*[^{};]*\{\s*$"
    r")")


@dataclass
class Lang:
    exts: tuple
    comment: str
    style: str
    decl: object = None
    funcs: frozenset = frozenset()
    imp: object = None
    method: object = None      # keyword-less method shape (TS/JS/Java/C#); optional


def _c(p):
    return re.compile(p) if p else None


LANGS = {
    "python": Lang((".py",), "#", "indent",
                   _c(r"^(async def|def|class)\s+(\w+)"), frozenset({"def", "async def"}),
                   _c(r"^\s*(?:from\s+([.\w]+)\s+import|import\s+([.\w]+))")),
    "swift": Lang((".swift",), "//", "brace",
                  _c(rf"^\s*{_MODS}\b(struct|class|enum|actor|protocol|extension|func)\s+([\w.]+)"),
                  frozenset({"func"}), _c(r"^\s*import\s+(\w+)")),
    # TS/JS use explicit prefixes (not the lazy word-skip) so `for (const x of …)`
    # and `if (let …)` aren't mistaken for top-level const/let declarations.
    # const/let are NOT type/function kinds here (they'd catch local variables and
    # inflate decl counts); genuine arrow-function consts are caught by _METHOD.
    "ts": Lang((".ts", ".tsx", ".mts", ".cts"), "//", "brace",
               _c(r"^\s*(?:export\s+)?(?:default\s+)?(?:declare\s+)?(?:abstract\s+)?"
                  r"(function|class|interface|type|enum)\s*\*?\s+(\w+)"),
               frozenset({"function"}), _c(r"""^\s*import\s+.*?from\s+['"]([^'"]+)['"]"""), _METHOD),
    "js": Lang((".js", ".jsx", ".mjs", ".cjs"), "//", "brace",
               _c(r"^\s*(?:export\s+)?(?:default\s+)?(function|class)\s*\*?\s+(\w+)"),
               frozenset({"function"}), _c(r"""^\s*import\s+.*?from\s+['"]([^'"]+)['"]"""), _METHOD),
    "go": Lang((".go",), "//", "brace",
               _c(r"^\s*(func|type)\s+(?:\([^)]*\)\s*)?(\w+)"), frozenset({"func"}),
               _c(r'^\s*(?:[\w.]+\s+)?"([^"]+)"')),
    "rust": Lang((".rs",), "//", "brace",
                 _c(rf"^\s*{_MODS}\b(fn|struct|enum|trait|impl|mod)\s+(\w+)"),
                 frozenset({"fn"}), _c(r"^\s*use\s+([\w:]+)")),
    "java": Lang((".java",), "//", "brace",
                 _c(rf"^\s*{_MODS}\b(class|interface|enum|record)\s+(\w+)"),
                 frozenset(), _c(r"^\s*import\s+([\w.]+)"), _METHOD),
    "kotlin": Lang((".kt", ".kts"), "//", "brace",
                   _c(rf"^\s*{_MODS}\b(fun|class|object|interface)\s+(\w+)"),
                   frozenset({"fun"}), _c(r"^\s*import\s+([\w.]+)")),
    "cpp": Lang((".c", ".h", ".cc", ".cpp", ".cxx", ".hpp", ".hh"), "//", "brace",
                _c(rf"^\s*{_MODS}\b(class|struct|enum|namespace|union)\s+(\w+)"),
                frozenset(), _c(r'^\s*#\s*include\s+[<"]([^>"]+)')),
    "csharp": Lang((".cs",), "//", "brace",
                   _c(rf"^\s*{_MODS}\b(class|interface|enum|struct|record)\s+(\w+)"),
                   frozenset(), _c(r"^\s*using\s+([\w.]+)"), _METHOD),
    "ruby": Lang((".rb",), "#", "indent",
                 _c(r"^(def|class|module)\s+([\w.:?!=]+)"), frozenset({"def"}),
                 _c(r"""^\s*require(?:_relative)?\s+['"]([^'"]+)""")),
}
EXT_TO_LANG = {ext: name for name, spec in LANGS.items() for ext in spec.exts}

IGNORE_DIRS = {".git", ".venv", "venv", "__pycache__", "node_modules", "build",
               "dist", "DerivedData", ".build", "coverage", ".next", "Pods",
               ".mypy_cache", ".pytest_cache"}
MAX_FILE_BYTES = 1_500_000

# Universal "this file is machine-generated" markers (GitHub linguist / Go / etc.).
# Requires a real comment prefix and a *qualified* "generated" phrase, anchored at
# line start, checked only in the file's head — so authored prose that merely
# mentions "generated" or "do not edit" isn't mistaken for a generated blob.
_GENERATED = re.compile(
    r"(?i)^\s*(?://+|#+|/\*+|\*+|--+|;+|<!--)\s*"
    r"(?:@generated\b|generated by\b|code generated\b|auto-?generated\b"
    r"|automatically generated\b|this file (?:is|was) (?:auto(?:matically)?[ -]?)?generated\b)")


def _looks_generated(head: str) -> bool:
    return any(_GENERATED.match(line) for line in head.splitlines()[:5])


# Coupling thresholds (behavioral-code-analysis defaults).
COUPLING_SUPPORT = 4          # ignore pairs seen fewer than this many times
COUPLING_RATIO = 0.5          # report only strong co-change
COUPLING_MAX_PER_COMMIT = 40  # bulk/mechanical commits aren't real coupling


# --------------------------------------------------------------------------- #
# Data model                                                                  #
# --------------------------------------------------------------------------- #

@dataclass
class FileRec:
    path: str
    lang: str
    loc: int = 0           # non-blank lines
    total_lines: int = 0
    code: int = 0
    comment: int = 0
    density: float = 0.0
    n_decls: int = 0
    largest_symbol: str = ""
    largest_symbol_lines: int = 0   # non-blank lines in the biggest symbol
    longest_func: str = ""
    longest_func_lines: int = 0     # non-blank lines in the biggest function
    max_nesting: int = 0
    n_imports: int = 0
    todo: int = 0
    churn: int = 0
    authors: int = 0
    last_touched_days: "int | None" = None
    god_ratio: float = 0.0
    hotspot: int = 0
    pct: dict = field(default_factory=dict)


# --------------------------------------------------------------------------- #
# Discovery (git working tree, with a filesystem fallback)                     #
# --------------------------------------------------------------------------- #

def discover(repo: Path, paths, only_lang):
    """Return ([(rel_path, lang)], coverage). Coverage is honest about what was
    skipped (generated / oversized) so a partial scan never reads as 'covered
    everything'."""
    files = git_tree(repo)
    if files is None:                       # not a git repo — walk the filesystem
        files = []
        for p in repo.rglob("*"):
            if p.is_file() and not any(part in IGNORE_DIRS for part in p.parts):
                files.append(str(p.relative_to(repo)))
    out = []
    cov = {"skipped_big": 0, "generated": 0, "by_lang": defaultdict(int),
           "roots": set(), "unknown_ext": 0}
    for rel in files:
        relp = rel.replace("\\", "/")
        lang = EXT_TO_LANG.get(Path(rel).suffix)
        if lang is None:
            cov["unknown_ext"] += 1
            continue
        if only_lang and lang != only_lang:
            continue
        if paths and not any(relp.startswith(pre) for pre in paths):
            continue
        ap = repo / rel
        try:
            if ap.stat().st_size > MAX_FILE_BYTES:
                cov["skipped_big"] += 1
                continue
            head = ap.open("r", encoding="utf-8", errors="replace").read(2048)
        except OSError:
            continue
        if _looks_generated(head):
            cov["generated"] += 1
            continue
        out.append((rel, lang))
        cov["by_lang"][lang] += 1
        cov["roots"].add(Path(rel).parts[0] if len(Path(rel).parts) > 1 else ".")
    return out, cov


def git_tree(repo: Path):
    try:
        out = subprocess.run(
            ["git", "ls-files", "--cached", "--others", "--exclude-standard"],
            cwd=repo, capture_output=True, text=True, timeout=60)
        return [l for l in out.stdout.splitlines() if l.strip()] if out.returncode == 0 else None
    except (subprocess.SubprocessError, OSError):
        return None


# --------------------------------------------------------------------------- #
# Static analysis                                                             #
# --------------------------------------------------------------------------- #

def strip_strings(line, ml, comment):
    """Remove string / char / template / comment content (tracking multi-line
    strings and block comments across lines via `ml`) so their braces never skew
    brace-depth. Returns (code_only, new_ml). This is what makes brace sizing
    robust to template literals and triple-quoted strings."""
    out = []
    i, n = 0, len(line)
    while i < n:
        if ml is not None:                       # inside a multi-line string/comment
            j = line.find(ml, i)
            if j < 0:
                return "".join(out), ml
            i, ml = j + len(ml), None
            continue
        if comment and line.startswith(comment, i):
            break
        if line.startswith("/*", i):
            j = line.find("*/", i + 2)
            if j < 0:
                return "".join(out), "*/"
            i = j + 2
            continue
        if line.startswith('"""', i) or line.startswith("'''", i):
            t = line[i:i + 3]
            j = line.find(t, i + 3)
            if j < 0:
                return "".join(out), t
            i = j + 3
            continue
        ch = line[i]
        if ch in "\"'`":
            j = i + 1
            while j < n:
                if line[j] == "\\":
                    j += 2
                    continue
                if line[j] == ch:
                    break
                j += 1
            if j >= n:
                return "".join(out), ("`" if ch == "`" else None)
            i = j + 1
            continue
        out.append(ch)
        i += 1
    return "".join(out), ml


def _nb(cum, start, end):
    """Non-blank line count within [start, end] from a cumulative array."""
    return cum[end + 1] - cum[start]


def _record(rec, label, name, span, is_func):
    rec.n_decls += 1
    if span > rec.largest_symbol_lines:
        rec.largest_symbol_lines, rec.largest_symbol = span, f"{label} {name}"
    if is_func and span > rec.longest_func_lines:
        rec.longest_func_lines, rec.longest_func = span, name


def analyze_brace(rec, lines, spec, cum):
    """Size symbols by brace depth, at every nesting level (so a method inside a
    class is measured, not just the class). A pending decl attaches to the next
    `{`; its symbol closes when that brace's match is popped."""
    decl_re, funcs, comment, method_re = spec.decl, spec.funcs, spec.comment, spec.method
    depth = max_depth = 0
    ml = None
    pending = None      # (label, name, start, is_func) awaiting its opening brace
    stack = []          # one frame per open `{`: the decl that brace opened, or None
    for i, raw in enumerate(lines):
        incoming = ml
        code, ml = strip_strings(raw, ml, comment)
        if incoming is None:
            if decl_re and (m := decl_re.match(raw)):
                pending = (m.group(1), m.group(2), i, m.group(1) in funcs)
            elif method_re and (m := method_re.match(raw)):
                pending = ("func", m.group(1) or m.group(2), i, True)
        for ch in code:
            if ch == "{":
                stack.append(pending)
                pending = None
                depth += 1
                if depth > max_depth:
                    max_depth = depth
            elif ch == "}":
                if stack:
                    decl = stack.pop()
                    depth -= 1
                    if decl:
                        _record(rec, decl[0], decl[1], _nb(cum, decl[2], i), decl[3])
                elif depth > 0:
                    depth -= 1
    rec.max_nesting = max_depth


def analyze_indent(rec, lines, spec, cum):
    """Size def/class by indentation, at every level. A decl owns lines until the
    next non-blank, non-comment line at an equal-or-lower indent."""
    decl_re, funcs, marker = spec.decl, spec.funcs, spec.comment
    n = len(lines)
    decls = []
    for i, raw in enumerate(lines):
        s = raw.lstrip()
        if not s or s.startswith(marker):
            continue
        if m := decl_re.match(s):
            decls.append((m.group(1), m.group(2), i, len(raw) - len(s)))
    for kind, name, start, indent in decls:
        end = n - 1
        for j in range(start + 1, n):
            t = lines[j]
            ts = t.lstrip()
            if ts and not ts.startswith(marker) and len(t) - len(ts) <= indent:
                end = j - 1
                break
        while end > start and not lines[end].strip():
            end -= 1
        _record(rec, kind, name, _nb(cum, start, end), kind in funcs)
    max_indent = 0
    for raw in lines:
        s = raw.lstrip()
        if s and not s.startswith(marker):
            max_indent = max(max_indent, (len(raw) - len(s)) // 4)
    rec.max_nesting = max_indent


def analyze_file(repo, rel, lang):
    try:
        text = (repo / rel).read_text(encoding="utf-8", errors="replace")
    except OSError:
        return None
    spec = LANGS[lang]
    rec = FileRec(path=rel, lang=lang)
    lines = text.splitlines()
    rec.total_lines = len(lines)
    marker = spec.comment
    cum = [0] * (len(lines) + 1)        # cumulative non-blank line count
    nonblank_chars, in_block = 0, False
    for idx, raw in enumerate(lines):
        s = raw.strip()
        cum[idx + 1] = cum[idx] + (1 if s else 0)
        if not s:
            continue
        rec.loc += 1
        nonblank_chars += len(raw.rstrip())
        is_comment = False
        if spec.style == "brace" and ("/*" in s or in_block):
            is_comment = True
            if "/*" in s and "*/" not in s:
                in_block = True
            elif "*/" in s:
                in_block = False
        elif s.startswith(marker):
            is_comment = True
        rec.comment += is_comment
        rec.code += not is_comment
        if "TODO" in s or "FIXME" in s:
            rec.todo += 1
        if spec.imp and spec.imp.match(raw):
            rec.n_imports += 1
    (analyze_brace if spec.style == "brace" else analyze_indent)(rec, lines, spec, cum)
    rec.god_ratio = round(min(1.0, rec.largest_symbol_lines / rec.loc), 2) if rec.loc else 0.0
    rec.density = round(nonblank_chars / rec.loc, 1) if rec.loc else 0.0
    return rec


