# Git-temporal signals                                                        #
# --------------------------------------------------------------------------- #

def git_history(repo, since_days):
    try:
        out = subprocess.run(
            ["git", "log", f"--since={since_days} days ago", "--no-merges",
             "--name-only", "--pretty=format:\x01%an\x02%ct"],
            cwd=repo, capture_output=True, text=True, timeout=120).stdout
    except (subprocess.SubprocessError, OSError):
        return {}, {}, {}, {}, {}, None
    churn, authors, last_ts = defaultdict(int), defaultdict(set), {}
    pair, count = defaultdict(int), defaultdict(int)
    head_ts = author = ts = None
    files = []

    def flush():
        nonlocal files
        for f in files:
            churn[f] += 1
            authors[f].add(author)
            count[f] += 1
            if ts and (f not in last_ts or ts > last_ts[f]):
                last_ts[f] = ts
        if 1 < len(files) <= COUPLING_MAX_PER_COMMIT:
            u = sorted(set(files))
            for a in range(len(u)):
                for b in range(a + 1, len(u)):
                    pair[(u[a], u[b])] += 1
        files = []

    for line in out.splitlines():
        if line.startswith("\x01"):
            flush()
            author, ts = line[1:].split("\x02")
            ts = int(ts)
            head_ts = head_ts or ts
        elif line.strip():
            files.append(line.strip())
    flush()
    return churn, {f: len(a) for f, a in authors.items()}, last_ts, pair, count, head_ts


# --------------------------------------------------------------------------- #
# Ranking + coupling                                                          #
# --------------------------------------------------------------------------- #

def pctile(values, v):
    """Mid-rank percentile: ties share the middle of their band, and the top value
    reports near 100 rather than an artificially low number."""
    if not values:
        return 0
    below = sum(1 for x in values if x < v)
    equal = sum(1 for x in values if x == v)
    return round(100 * (below + 0.5 * equal) / len(values))


def attach_percentiles(recs):
    sigs = ["loc", "largest_symbol_lines", "longest_func_lines", "max_nesting",
            "churn", "hotspot", "god_ratio", "density"]
    by_lang = defaultdict(list)
    for r in recs:
        by_lang[r.lang].append(r)
    for group in by_lang.values():
        pools = {s: [getattr(r, s) for r in group] for s in sigs}
        for r in group:
            r.pct = {s: pctile(pools[s], getattr(r, s)) for s in sigs}


def dist(values):
    if not values:
        return {"p50": 0, "p90": 0, "p99": 0, "max": 0}
    sv = sorted(values)
    q = lambda p: sv[min(len(sv) - 1, int(p * len(sv)))]
    return {"p50": q(.5), "p90": q(.9), "p99": q(.99), "max": sv[-1]}


def module(path, depth):
    parts = Path(path).parts
    return "/".join(parts[:depth]) if len(parts) > depth else "/".join(parts[:-1]) or "."


# --------------------------------------------------------------------------- #
# Render                                                                       #
# --------------------------------------------------------------------------- #

def pp(v):
    return f"{v:>3}"


def render_md(recs, cov, since_days, commits, coupling, top):
    L, by_lang = [], defaultdict(list)
    for r in recs:
        by_lang[r.lang].append(r)
    L.append("# Codebase audit\n")
    L.append("_Signal for judgment, not a gate. Sizes are proxies; brace/indent spans are approximate._\n")
    langs = ", ".join(f"{k} {len(v)}" for k, v in sorted(by_lang.items()))
    L.append(f"- Files: {len(recs)}  ·  non-blank LOC: {sum(r.loc for r in recs):,}  ·  "
             f"git window: {since_days}d ({commits} commits)")
    L.append(f"- Languages: {langs}")
    L.append(f"- Roots: {', '.join(sorted(cov['roots']))}")
    skip = []
    if cov["generated"]:
        skip.append(f"{cov['generated']} generated")
    if cov["skipped_big"]:
        skip.append(f"{cov['skipped_big']} file(s) >{MAX_FILE_BYTES // 1000}KB")
    if skip:
        L.append(f"- Skipped: {'; '.join(skip)}")
    L.append("")

    L.append("## Distributions (per language)\n")
    L.append("| lang | files | LOC p50/p90/p99/max | maxFn p90/max | nest p90/max | dens p50/p90/max | churn p90/max |")
    L.append("|---|--:|---|---|---|---|---|")
    for lang, g in sorted(by_lang.items()):
        dl, df = dist([r.loc for r in g]), dist([r.longest_func_lines for r in g])
        dn, dd, dc = dist([r.max_nesting for r in g]), dist([r.density for r in g]), dist([r.churn for r in g])
        L.append(f"| {lang} | {len(g)} | {dl['p50']}/{dl['p90']}/{dl['p99']}/{dl['max']} | {df['p90']}/{df['max']} "
                 f"| {dn['p90']}/{dn['max']} | {dd['p50']}/{dd['p90']}/{dd['max']} | {dc['p90']}/{dc['max']} |")

    L.append(f"\n## Attention leaderboard (top {top} by hotspot = LOC x churn)\n")
    L.append("Percentiles are within-language. `god%` = biggest symbol's share of file; "
             "`maxFn` = biggest single function; `decls` = top-level + nested symbols.\n")
    L.append("| file | LOC | LOC&deg; | maxSym | god% | maxFn | decls | c/l | nest&deg; | churn&deg; | last |")
    L.append("|---|--:|--:|---|--:|---|--:|--:|--:|--:|--:|")
    for r in sorted(recs, key=lambda r: (r.hotspot, r.loc), reverse=True)[:top]:
        last = f"{r.last_touched_days}d" if r.last_touched_days is not None else "-"
        sym = f"{r.largest_symbol} ({r.largest_symbol_lines})" if r.largest_symbol else "-"
        fn = f"{r.longest_func} ({r.longest_func_lines})" if r.longest_func else "-"
        L.append(f"| `{r.path}` | {r.loc} | {pp(r.pct['loc'])} | {sym} | {int(r.god_ratio*100)} | {fn} | {r.n_decls} "
                 f"| {r.density} | {pp(r.pct['max_nesting'])} | {pp(r.pct['churn'])} | {last} |")

    gods = sorted([r for r in recs if r.god_ratio >= .5 and r.largest_symbol_lines >= 60],
                  key=lambda r: r.largest_symbol_lines, reverse=True)[:top]
    if gods:
        L.append("\n## God-symbol files (one symbol is most of the file)\n")
        L.append("| file | symbol | symbol LOC | biggest fn | file LOC | god% |")
        L.append("|---|---|--:|---|--:|--:|")
        for r in gods:
            fn = f"{r.longest_func} ({r.longest_func_lines})" if r.longest_func else "-"
            L.append(f"| `{r.path}` | {r.largest_symbol} | {r.largest_symbol_lines} | {fn} | {r.loc} | {int(r.god_ratio*100)} |")

    if coupling:
        L.append("\n## Temporal coupling (co-change ≥ 50%, support ≥ 4)\n")
        L.append("Files that change together. Cross-module pairs (&starf;) suggest a boundary the tree doesn't show.\n")
        L.append("| file A | file B | co-changes | A&rarr;B | B&rarr;A | |")
        L.append("|---|---|--:|--:|--:|:-:|")
        for c in coupling[:top]:
            L.append(f"| `{c['a']}` | `{c['b']}` | {c['n']} | {int(c['ab']*100)}% | {int(c['ba']*100)}% "
                     f"| {'&starf;' if c['cross'] else ''} |")

    L.append("\n---\n_How to read: start at the leaderboard. High LOC&deg; + low god% (or small `maxFn` + many "
             "`decls`) = big but flat — a registry of small parts, often fine. High god% with a large `maxFn`, "
             "or high `nest&deg;` = one symbol/function eating the file: a refactor candidate. High churn&deg; = "
             "where bugs accrue. Coupling stars = candidate module splits. Decide per file; nothing here is a verdict._")
    return "\n".join(L)


