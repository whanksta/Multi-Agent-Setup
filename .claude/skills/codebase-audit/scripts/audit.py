# Changed-files view (post-commit heads-up)                                    #
# --------------------------------------------------------------------------- #

def changed_files(repo, staged):
    """Files to spotlight: the staged diff (pre-commit, the commit being made) or
    HEAD's commit (post-hoc). staged=True has no churn self-inflation since the
    commit doesn't exist yet."""
    cmd = (["git", "diff", "--cached", "--name-only"] if staged
           else ["git", "diff-tree", "--no-commit-id", "--name-only", "-r", "--root", "HEAD"])
    try:
        out = subprocess.run(cmd, cwd=repo, capture_output=True, text=True, timeout=30)
        return [l.strip() for l in out.stdout.splitlines() if l.strip()] if out.returncode == 0 else []
    except (subprocess.SubprocessError, OSError):
        return []


def outlier_reasons(r):
    """Transparent, evidence-not-verdict heads-up: which signals put this file in
    its language's top decile (or gave it a big single function). Never a gate."""
    reasons = []
    if r.pct.get("loc", 0) >= 90:
        reasons.append(f"p{r.pct['loc']} LOC")
    if r.pct.get("max_nesting", 0) >= 90:
        reasons.append(f"p{r.pct['max_nesting']} nesting")
    if r.pct.get("churn", 0) >= 90:
        reasons.append(f"p{r.pct['churn']} churn")
    if r.god_ratio >= 0.6 and r.longest_func_lines >= 60:
        reasons.append(f"god {int(r.god_ratio*100)}% / maxFn {r.longest_func}({r.longest_func_lines})")
    return reasons


def render_changed(recs, changed):
    by_path = {r.path: r for r in recs}
    src = [by_path[p] for p in changed if p in by_path]
    flagged = [(r, rs) for r in src if (rs := outlier_reasons(r))]
    flagged.sort(key=lambda x: x[0].hotspot, reverse=True)
    other = len(changed) - len(src)
    head = f"codebase-audit · {len(src)} source file(s) changed" + (f", {other} other" if other else "")
    if not flagged:
        return f"{head} — all within normal ranges (advisory, non-blocking)"
    L = [f"{head} (advisory, non-blocking — `audit.py` for the full map):"]
    for r, rs in flagged:
        ctx = []
        if r.largest_symbol:
            ctx.append(f"maxSym {r.largest_symbol}({r.largest_symbol_lines})")
        ctx.append(f"{r.n_decls} decls")
        L.append(f"  ⚠ {r.path}")
        L.append(f"      {' · '.join(rs)}  ·  {' · '.join(ctx)}")
    if len(src) > len(flagged):
        L.append(f"  {len(src) - len(flagged)} other touched file(s) within normal ranges")
    return "\n".join(L)


