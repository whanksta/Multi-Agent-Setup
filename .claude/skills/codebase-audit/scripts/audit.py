def main():
    ap = argparse.ArgumentParser(description="Codebase health audit (signal, not a gate).")
    ap.add_argument("--path", action="append", help="scope to a subtree (repeatable, prefix match)")
    ap.add_argument("--lang", help=f"restrict to one language: {'|'.join(sorted(LANGS))}")
    ap.add_argument("--since-days", type=int, default=90, help="git window for churn/coupling (default 90)")
    ap.add_argument("--top", type=int, default=15, help="rows per leaderboard (default 15)")
    ap.add_argument("--module-depth", type=int, default=2, help="path segments that define a 'module' for coupling (default 2)")
    ap.add_argument("--changed", action="store_true", help="heads-up scoped to HEAD's commit, flagged if structural outliers (non-blocking)")
    ap.add_argument("--staged", action="store_true", help="like --changed but scoped to the staged diff (for a pre-commit hook)")
    ap.add_argument("--json", action="store_true", help="emit machine-readable JSON")
    ap.add_argument("--repo", default=".", help="repo root (default cwd)")
    args = ap.parse_args()
    if args.lang and args.lang not in LANGS:
        sys.exit(f"--lang must be one of {sorted(LANGS)}")

    repo = Path(args.repo).resolve()
    paths = [p.replace("\\", "/").rstrip("/") for p in (args.path or [])]

    found, cov = discover(repo, paths, args.lang)
    recs = [r for rel, lang in found if (r := analyze_file(repo, rel, lang))]
    if not recs:
        sys.exit("No source files found. Is this a git repo? Try --path or --repo.")

    churn, authors, last_ts, pair, count, head_ts = git_history(repo, args.since_days)
    by_path = {r.path: r for r in recs}
    for r in recs:
        r.churn, r.authors = churn.get(r.path, 0), authors.get(r.path, 0)
        if r.path in last_ts and head_ts:
            r.last_touched_days = max(0, (head_ts - last_ts[r.path]) // 86400)
        r.hotspot = r.loc * r.churn
    attach_percentiles(recs)

    if args.changed or args.staged:
        print(render_changed(recs, changed_files(repo, args.staged)))
        return

    coupling = []
    for (a, b), n in pair.items():
        if n < COUPLING_SUPPORT or a not in by_path or b not in by_path:
            continue
        ab, ba = n / (count.get(a, n) or n), n / (count.get(b, n) or n)
        if max(ab, ba) < COUPLING_RATIO:
            continue
        coupling.append({"a": a, "b": b, "n": n, "ab": round(ab, 2), "ba": round(ba, 2),
                         "cross": module(a, args.module_depth) != module(b, args.module_depth)})
    coupling.sort(key=lambda c: (c["cross"], c["n"]), reverse=True)
    commits = commit_count(repo, args.since_days)

    if args.json:
        print(json.dumps({
            "files": [vars(r) for r in recs], "coupling": coupling,
            "meta": {"since_days": args.since_days, "commits": commits,
                     "languages": dict(cov["by_lang"]), "generated": cov["generated"],
                     "skipped_big": cov["skipped_big"]},
        }, indent=2, default=str))
    else:
        print(render_md(recs, cov, args.since_days, commits, coupling, args.top))


def commit_count(repo, since_days):
    try:
        return int(subprocess.run(
            ["git", "rev-list", "--count", "--no-merges", f"--since={since_days} days ago", "HEAD"],
            cwd=repo, capture_output=True, text=True, timeout=30).stdout.strip() or 0)
    except (subprocess.SubprocessError, OSError, ValueError):
        return 0


if __name__ == "__main__":
    main()
